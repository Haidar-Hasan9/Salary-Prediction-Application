import streamlit as st
import pandas as pd
from supabase import create_client
from dotenv import load_dotenv
import os
import base64
from io import BytesIO
from PIL import Image

load_dotenv()

# Supabase credentials (must be in .env)
SUPABASE_URL = os.getenv("SUPABASE_URL")
SUPABASE_KEY = os.getenv("SUPABASE_KEY")

if not SUPABASE_URL or not SUPABASE_KEY:
    st.error("Supabase credentials missing. Check .env file.")
    st.stop()

supabase = create_client(SUPABASE_URL, SUPABASE_KEY)

st.set_page_config(page_title="Salary Prediction Dashboard", layout="wide")
st.title("📊 Data Science Salary Prediction Dashboard")
st.markdown("Predictions generated by a Decision Tree model, analyzed by LLM (Ollama).")

@st.cache_data(ttl=60)
def load_data():
    """Fetch all predictions from Supabase."""
    try:
        response = supabase.table("predictions").select("*").execute()
        df = pd.DataFrame(response.data)
        return df
    except Exception as e:
        st.error(f"Failed to load data: {e}")
        return pd.DataFrame()

df = load_data()

if df.empty:
    st.warning("No data found in Supabase. Run the LLM pipeline first.")
    st.stop()

# ---- Sidebar filters ----
st.sidebar.header("Filter Predictions")
exp_levels = st.sidebar.multiselect("Experience Level", df["experience_level"].unique(), default=df["experience_level"].unique())
job_titles = st.sidebar.multiselect("Job Title", df["job_title"].unique(), default=df["job_title"].unique())
countries = st.sidebar.multiselect("Country", df["employee_residence"].unique(), default=df["employee_residence"].unique())

filtered_df = df[
    (df["experience_level"].isin(exp_levels)) &
    (df["job_title"].isin(job_titles)) &
    (df["employee_residence"].isin(countries))
]

# ---- Main area ----
col1, col2 = st.columns([2, 1])

with col1:
    st.subheader("📋 Predictions")
    st.dataframe(filtered_df[["experience_level", "job_title", "work_year", "employee_residence", "predicted_salary_usd"]], use_container_width=True)

    st.subheader("📈 Salary Statistics")
    if not filtered_df.empty:
        avg_salary = filtered_df["predicted_salary_usd"].mean()
        st.metric("Average Predicted Salary", f"${avg_salary:,.2f}")

with col2:
    st.subheader("🤖 LLM Narrative")
    # Take the first row's narrative (all rows have the same narrative)
    narrative = df.iloc[0]["llm_narrative"] if "llm_narrative" in df.columns and not df.empty else "No narrative available."
    st.markdown(narrative)

    st.subheader("📊 Chart")
    # Decode base64 and display image
    if "chart_base64" in df.columns and not df.empty and df.iloc[0]["chart_base64"]:
        chart_b64 = df.iloc[0]["chart_base64"]
        image_data = base64.b64decode(chart_b64)
        image = Image.open(BytesIO(image_data))
        st.image(image, caption="Salary by Experience Level")
    else:
        st.info("No chart available.")

# ---- Optional: Show a row selector for detailed view ----
st.subheader("🔍 Detailed View")
selected_idx = st.selectbox("Select a row to see full details", filtered_df.index)
if selected_idx is not None:
    row = filtered_df.loc[selected_idx]
    st.json({
        "Experience Level": row["experience_level"],
        "Job Title": row["job_title"],
        "Work Year": row["work_year"],
        "Country": row["employee_residence"],
        "Predicted Salary (USD)": f"${row['predicted_salary_usd']:,.2f}",
        "LLM Narrative": row.get("llm_narrative", "N/A")[:500] + "...",
    })